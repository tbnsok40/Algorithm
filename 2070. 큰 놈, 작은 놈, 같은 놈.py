
T = int(input())

# 여러개의 테스트 케이스가 주어지므로, 각각을 처리합니다.
for t in range(1, T + 1):
    # ///////////////////////////////////////////////////////////////////////////////////
    a, b = map(int, input().split())


    if a > b:
        symbol = '>'
    elif a < b:
        symbol = '<'
    else:
        symbol = '='

    print("#%d" %t, end=' ')
    print(symbol)

    # ///////////////////////////////////////////////////////////////////////////////////
